"""所有會議摘要風格共用的事實約束與輸出品質規則。"""

SUMMARY_GROUNDING_RULES = """\
# 全域摘要規則（優先於其他指示）
1. 一律使用繁體中文，保留必要的英文產品名、型號、版本與專有名詞。
2. 只能根據逐字稿內容整理，不得補寫、猜測或創造未出現的事實。
3. 日期、參與者、負責人、期限、數字或決議若無法確認，明確填寫「未提供」或「待確認」。
4. 嚴格區分「已決議」、「提案／建議」、「仍在討論」與「未決問題」，不可把意見誤寫成決議。
5. 保留關鍵數字、日期、金額、比例、版本及否定語意；不得為了簡化而改變原意。
6. 聽辨內容疑似錯誤或語意不完整時，以「[聽辨不確定]」標示，不自行修補成看似合理的內容。
7. 合併重複議題，但保留相互衝突的觀點，並標示尚未取得共識。
8. 待辦必須是可執行動作；負責人或期限不明時不得自行推定。
9. 不輸出冗長逐字引用；只在支持重要決議或釐清爭議時保留短句。
10. 若逐字稿不足以產生某個區塊，保留該區塊並寫「無明確資訊」，不要省略或虛構。
"""


def apply_summary_rules(rendered_prompt: str) -> str:
    """將共同事實約束置於風格提示之前，降低摘要幻覺與錯誤歸因。"""

    return f"{SUMMARY_GROUNDING_RULES}\n\n{rendered_prompt}"
